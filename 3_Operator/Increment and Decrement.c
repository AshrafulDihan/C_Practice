#include <stdio.h>

int main() {
    int x = 5;

    printf("Value of x is = %d\n", x);
    printf("After x++ (post-increment): x = %d\n", x++);
    printf("After ++x (pre-increment): x = %d\n", ++x);
    printf("After x-- (post-decrement): x = %d\n", x--);
    printf("After --x (pre-decrement): x = %d\n", --x);
    return 0;
}
